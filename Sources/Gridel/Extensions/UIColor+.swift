import UIKit

extension UIColor {

    static var background: UIColor {
        return UIColor(named: "background") ?? .red
    }

    static var primary1: UIColor {
        return UIColor(named: "primary1") ?? .red
    }

    static var primary2: UIColor {
        return UIColor(named: "primary2") ?? .red
    }

    static var secondary1: UIColor {
        return UIColor(named: "secondary1") ?? .red
    }

}
